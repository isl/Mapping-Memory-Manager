package isl.FIMS.filter;

import java.io.IOException;
import java.net.URLEncoder;
import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import javax.servlet.http.Cookie;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import javax.servlet.http.HttpSession;

public class LoginFilter implements Filter {

    private FilterConfig filterConfig = null;

    @Override
    public void init(FilterConfig filterConfig) throws ServletException {
        this.filterConfig = filterConfig;
    }

    @Override
    public void destroy() {
        this.filterConfig = null;
    }

    @Override
    public void doFilter(ServletRequest request, ServletResponse response, FilterChain chain) throws IOException, ServletException {

        if (request instanceof HttpServletRequest) {
            HttpServletRequest hrequest = (HttpServletRequest) request;
            HttpSession session = hrequest.getSession();
            String username = (String) session.getAttribute("username");
            HttpServletResponse hresponse = (HttpServletResponse) response;
            if (username == null) {
                hresponse.sendRedirect("Login");
            } else {
                String sessionId = session.getId();
                String encodeUsername = URLEncoder.encode(username);
                Cookie cookie = new Cookie(encodeUsername, sessionId);
                String editorWebapp = session.getServletContext().getInitParameter("editorWebapp");
                cookie.setPath("/"+editorWebapp );
                cookie.setMaxAge(1800);
                hresponse.addCookie(cookie);
                chain.doFilter(hrequest, hresponse);
            }
        }
    }
}
